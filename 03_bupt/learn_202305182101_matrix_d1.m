% 1 一维数组（向量）的创建和提取

% 1.1 直接输入
a = [1;2;3]
b = [1,2,3]
c = [1 2 3]

% 1.2 冒号生成法
% 用于生成递增或递减的等差数列
% 步长默认是1
a2 = 1:2:6
b2 = 1:6

% 1.3 定数现行采样法
% 在1和2之间产生10个点
% n的默认值是100
a3 = linspace(1, 2, 10)

% 1.4 拼接法
a4 = [b, c]
b4 = [a; a]

% 向量抽取
tmp = 1:10
asub = tmp(1:2:end)

% 2. 一维数组中元素的提取
A2 = 1:5
a23 = A2(3)


